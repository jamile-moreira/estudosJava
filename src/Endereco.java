public class Endereco {
    String rua;
    int numero;
    String bairro;
    String cidade;
    String estado;
    String cep;

    public Endereco(String rua, int numero, String bairro, String cidade, String estado, String cep){
        this.rua = rua;
        this.numero = numero;
        this.bairro = bairro;
        this.cidade = cidade;
        this.estado = estado;
        this.cep = cep;
    }
}
